/*
== igualdade valores
=== igualdade valores e tipos
!== diferença
> maior que
< menor que
>= maior ou igual
<= menor ou igual
*/
let a = 10
let b = 5

// == igualdade de valores
console.log(a == b) // false

// === igualdade de valores e tipos
console.log(a === b) // false

// !== diferença
console.log(a !== b) // true

// > maior que
console.log(a > b) // true

// < menor que
console.log(a < b) // false

// >= maior ou igual
console.log(a >= b) // true

// <= menor ou igual
console.log(a <= b) // false




